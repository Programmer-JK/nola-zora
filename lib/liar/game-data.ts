export function pickRandom<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

function filterCats(selected: string[]) {
  if (!selected.length) return WORD_CATEGORIES;
  const filtered = WORD_CATEGORIES.filter(c => selected.includes(c.category));
  return filtered.length ? filtered : WORD_CATEGORIES;
}

export function pickCategoryAndWord(selected: string[] = []): { category: string; word: string } {
  const cat = pickRandom(filterCats(selected));
  return { category: cat.category, word: pickRandom(cat.words) };
}

/** 같은 카테고리에서 서로 다른 단어 두 개를 뽑음 (거짓 라이어 모드용) */
export function pickCategoryAndTwoWords(selected: string[] = []): { category: string; word: string; liarWord: string } {
  const cat = pickRandom(filterCats(selected).filter(c => c.words.length >= 2));
  const wordIdx = Math.floor(Math.random() * cat.words.length);
  let altIdx = Math.floor(Math.random() * (cat.words.length - 1));
  if (altIdx >= wordIdx) altIdx++;
  return { category: cat.category, word: cat.words[wordIdx], liarWord: cat.words[altIdx] };
}

